    def get_search_type(self, query: str) -> str:
        """根据查询内容确定搜索类型
        
        Args:
            query: 用户查询
            
        Returns:
            搜索类型 (web, image, video)
        """
        query_lower = query.lower()
        
        # 检查是否需要图片搜索
        image_keywords = ["图片", "照片", "截图", "图片", "照片", "图像", "画面"]
        for keyword in image_keywords:
            if keyword in query_lower:
                return "image"
        
        # 检查是否需要视频搜索
        video_keywords = ["视频", "视频", "影片", "电影", "视频", "视频", "录像", "直播"]
        for keyword in video_keywords:
            if keyword in query_lower:
                return "video"
        
        # 默认使用网页搜索
        return "web"
    
    def get_time_filter(self, query: str) -> Optional[str]:
        """根据查询内容确定时间过滤
        
        Args:
            query: 用户查询
            
        Returns:
            时间过滤参数 (week, month, year, semiyear) 或 None
        """
        query_lower = query.lower()
        
        # 检查是否需要时间过滤
        if "最近一周" in query_lower or "一周内" in query_lower:
            return "week"
        elif "最近一个月" in query_lower or "一个月内" in query_lower:
            return "month"
        elif "最近半年" in query_lower or "半年内" in query_lower:
            return "semiyear"
        elif "最近一年" in query_lower or "一年内" in query_lower:
            return "year"
        
        # 检查是否包含时间关键词
        time_keywords = ["今天", "明天", "后天", "最近", "现在", "当前", "此刻"]
        for keyword in time_keywords:
            if keyword in query_lower:
                return "week"
        
        return None