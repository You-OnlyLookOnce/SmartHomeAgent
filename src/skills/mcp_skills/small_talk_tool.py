    def detect_topic(self, user_input: str) -> Tuple[bool, Optional[str], Optional[str]]:
        """检测用户输入是否为闲聊话题
        
        Args:
            user_input: 用户输入
            
        Returns:
            (是否为闲聊, 话题类型, 话题描述)
        """
        user_input_lower = user_input.lower()
        
        # 检查是否为闲聊话题
        for topic, rules in self.topic_rules.items():
            for keyword in rules['keywords']:
                if keyword in user_input:
                    return True, topic, rules['description']
        
        # 检查是否为任务导向型查询
        task_keywords = [
            '查询', '搜索', '天气', '时间', '日期', '计算', '导航', '地图',
            '设置', '配置', '帮助', '功能', '操作', '如何', '怎么', '什么',
            '为什么', '哪里', '什么时候', '谁', '多少', '多久'
        ]
        
        # 如果包含任务导向型关键词，但也可能是闲聊
        # 例如："今天天气怎么样" 可能是闲聊，也可能是任务查询
        # 这里我们需要进一步判断
        
        # 简单规则：如果输入长度较短，且主要是日常交流用语，则视为闲聊
        if len(user_input) < 20 and any(keyword in user_input for keyword in ['怎么样', '如何', '好吗', '好啊', '对啊', '是的', '不是']):
            return True, 'daily', '日常对话'
        
        # 默认为非闲聊
        return False, None, None
    
    def classify_emotion(self, user_input: str) -> str:
        """分类用户情感
        
        Args:
            user_input: 用户输入
            
        Returns:
            情感类型: positive, negative, neutral
        """
        positive_words = ['高兴', '开心', '快乐', '兴奋', '激动', '棒', '好', '不错', '优秀', '喜欢']
        negative_words = ['伤心', '难过', '生气', '愤怒', '焦虑', '紧张', '沮丧', '郁闷', '烦躁', '讨厌']
        
        user_input_lower = user_input.lower()
        
        for word in positive_words:
            if word in user_input:
                return 'positive'
        
        for word in negative_words:
            if word in user_input:
                return 'negative'
        
        return 'neutral'
    
    def generate_response(self, user_input: str, topic: str = None, emotion: str = None) -> str:
        """生成闲聊响应
        
        Args:
            user_input: 用户输入
            topic: 话题类型
            emotion: 情感类型
            
        Returns:
            闲聊响应
        """
        # 如果没有提供话题，自动检测
        if not topic:
            is_small_talk, topic, _ = self.detect_topic(user_input)
            if not is_small_talk:
                return "抱歉，我无法回答这个问题。如果你有其他问题，我很乐意帮助你。"
        
        # 如果没有提供情感，自动检测
        if not emotion:
            emotion = self.classify_emotion(user_input)
        
        # 根据话题和情感生成响应
        if topic == 'emotion':
            if emotion == 'positive':
                return random.choice(self.response_templates['emotion']['positive'])
            elif emotion == 'negative':
                return random.choice(self.response_templates['emotion']['negative'])
            else:
                return random.choice(self.response_templates['default'])
        elif topic in self.response_templates:
            return random.choice(self.response_templates[topic])
        else:
            return random.choice(self.response_templates['default'])
    
    def get_response(self, user_input: str) -> str:
        """获取闲聊响应
        
        Args:
            user_input: 用户输入
            
        Returns:
            闲聊响应
        """
        is_small_talk, topic, _ = self.detect_topic(user_input)
        if not is_small_talk:
            return "抱歉，我无法回答这个问题。如果你有其他问题，我很乐意帮助你。"
        
        emotion = self.classify_emotion(user_input)
        return self.generate_response(user_input, topic, emotion)

